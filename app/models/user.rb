class User < ActiveRecord::Base
  has_many :blogposts
  validates_presence_of :name, :email
  validates_length_of :name, :maximum => 48
  validates_format_of :email, :with => /\A([^@\s]+)@((?:[-a-z0-9]+\.)+[a-z]{2,})\Z/i
end